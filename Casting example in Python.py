num_str = "10"
num_int = int(num_str)  # casting string to integer

# Integer to float
num_float = float(num_int)  # casting integer to float

# Float to string
num_str2 = str(num_float)  # casting float to string

print("String to int:", num_int)
print("Int to float:", num_float)
print("Float to string:", num_str2)
